package ua.dp.ort.module3;

public class SwitchOperator {

    public static void main(String[] args) {
        
        int var = 2;
        
        switch(var) {
            case 1:     
                   System.out.println("choise 1");
                   break;
            case 2:
                    System.out.println("choise 2");

            default:
                    System.out.println("default");
        }

    }

}
